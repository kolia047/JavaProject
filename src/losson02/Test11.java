package losson02;

public class Test11 {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }
        System.out.println("--------------------------");
        int j = 10;
        while (j > 0) {
            System.out.println(j);
            j--;
        }
        System.out.println("-------------------------------");
        for (i = 2; i <= 100; i += 2) {
            System.out.println(i);
        }
        System.out.println("-------------------------------");
    }
}
